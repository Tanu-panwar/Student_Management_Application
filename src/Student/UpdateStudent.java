    private void cmbRollNoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbRollNoActionPerformed
        // TODO add your handling code here:
         try{
            Connection con = DBConnect1.connect();
            PreparedStatement ps = con.prepareStatement("select * from studData where rollno=?");
            ps.setString(1,cmbRollNo.getSelectedItem().toString()); 

            ResultSet rs = ps.executeQuery();
            while(rs.next()){
               txtName.setText(rs.getString(1));// name
               txtRoll.setText(rs.getString(2));// rollno
               txtID.setText(rs.getString(3));// email
               txtXII.setText(rs.getString(4));// xii
                // course
               course = rs.getString(5);
               txtAadhar.setText(rs.getString(6));// aadhar
               txtArea.setText(rs.getString(7));// address
               txtFather.setText(rs.getString(8));// fname
               ((JTextField)dcDOB.getDateEditor().getUiComponent()).setText(rs.getString(9));// dob
               txtPhn.setText(rs.getString(10));// phone
               txtX.setText(rs.getString(11));// x
                // branch
               branch = rs.getString(12);
                // image
                oldImage =rs.getBytes(13);
                lblImg.setIcon(resizePic(oldImage));
           }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_cmbRollNoActionPerformed

