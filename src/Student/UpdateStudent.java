    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(changeCourse==1){
            course = cmbCourse.getSelectedItem().toString();
        }
        if(changeBranch==1){
            branch = cmbBranch.getSelectedItem().toString();
        }
        try{
            Connection con = DBConnect1.connect();
            PreparedStatement ps = con.prepareStatement("update studData set name=?,rollno=?,"+
                    "email=?,xii=?,course=?,AadharNo=?,"+
                    "address=?,fname=?,dob=?,phone=?,x=?,branch=?,image=? where rollno=?");
            ps.setString(1,txtName.getText()); // name
            ps.setString(2,txtRoll.getText()); // roll no
            ps.setString(3,txtID.getText()); // email id
            ps.setString(4,txtXII.getText()); // class xii
            ps.setString(5,course); // course
            ps.setString(6,txtAadhar.getText()); // aadhar
            ps.setString(7,txtArea.getText()); // address
            ps.setString(8,txtFather.getText()); // father
            ps.setString(9,((JTextField)dcDOB.getDateEditor().getUiComponent()).getText()); // dob
            ps.setString(10,txtPhn.getText()); // phone
            ps.setString(11,txtX.getText()); // x class
            ps.setString(12,branch); // branch
            if(changeImage==1){
                InputStream ig = new FileInputStream(new File(imgPath));
                ps.setBlob(13,ig);
            }
            else{
                ps.setBlob(13,new ByteArrayInputStream(oldImage));
            }
            ps.setString(14,txtRoll.getText());
            int a = ps.executeUpdate();
            if(a==1){
                JOptionPane.showMessageDialog(null,"Successfully registered a new Student");
                dispose();
            }
            else
            JOptionPane.showMessageDialog(null,"Some Error");

        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

