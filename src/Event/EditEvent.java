    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try{
            Connection con = DBConnect1.connect();
            PreparedStatement ps = con.prepareStatement("select * from addEvent where date =?");
            Date date = dcDate.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String fd = sdf.format(date);
            ps.setString(1,fd);
            ResultSet rs = ps.executeQuery();
            int c12 = 0;
            while(rs.next()){
                c12++;
                cmbName.addItem(rs.getString("Name"));
                //txtAreaDesc.setString(rs.getString("Description"));
            }
            if(c12 == 0){
                JOptionPane.showMessageDialog(null,"No such event exist !");
            }
        }
        catch(Exception e){
            System.out.print(e);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try{
            Connection con = DBConnect1.connect();
            PreparedStatement ps = con.prepareStatement("update addEvent set Date =?, Name=?, description=? where id =?");
            Date date = dcDate.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String fd = sdf.format(date);
            ps.setString(1,fd);
            
            if(newName == null){
               ps.setString(2, cmbName.getSelectedItem().toString());
            }else{
               ps.setString(2, newName);
            }
            ps.setString(3,txtAreaDesc.getText());
            ps.setString(4,id);
            int a = ps.executeUpdate();
            if(a==1){
                JOptionPane.showMessageDialog(null, "Event Update Successfully !");
            }else{
                JOptionPane.showMessageDialog(null, "Something went wrong !");
            }
        }
        catch(Exception e){
            System.out.print(e);
        }
    }//GEN-LAST:event_btnEditActionPerformed

