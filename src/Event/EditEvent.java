    private void cmbNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbNameActionPerformed
        // TODO add your handling code here:
        try{
            Connection con = DBConnect1.connect();
            PreparedStatement ps = con.prepareStatement("select * from addevent where date =? and name=?");
            Date date = dcDate.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String fd = sdf.format(date);
            ps.setString(1,fd);
            ps.setString(2,cmbName.getSelectedItem().toString());
            ResultSet rs = ps.executeQuery();
            if(rs.next()){
               id = rs.getString("id");
               txtAreaDesc.setText(rs.getString("Description"));
            }else{
               JOptionPane.showMessageDialog(null, "No such Event Found!");
            }
        }catch(Exception e){
            System.out.print(e);
        }
    }//GEN-LAST:event_cmbNameActionPerformed

